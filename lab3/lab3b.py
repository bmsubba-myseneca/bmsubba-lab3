#!/usr/bin/env python3

# sum_numbers() function
def sum_numbers(a, b):
    return a + b

# subtract_numbers() function
def subtract_numbers(a, b):
    return a - b

# multiply_numbers() function
def multiply_numbers(a, b):
    return a * b

# Main Program (Optional, but not required by the tests)
if __name__ == '__main__':
    print("Sum:", sum_numbers(10, 5))
    print("Subtract:", subtract_numbers(10, 5))
    print("Multiply:", multiply_numbers(10, 5))
